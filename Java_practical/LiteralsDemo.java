public class LiteralsDemo
{
	public static void main(String[] args)
	{
		//integer literals
		int decimalLiterals=10;
		int binaryLiterals=0b110010;
		int octalalLiterals=0144;
		int hexLiterals=0x64;
		//floating point literals
		float floatliteral=2.718f;
		// character literals
		char charliteral='a';
		//string literals
		String stringliterals="Hello, World";
		//boolean literals
		boolean trueliterals=true;
		boolean falseliterals=false;
		//print literals.....
		System.out.println("decimalLiterals=" +decimalLiterals);
		System.out.println("binaryLiterals=" +binaryLiterals);
		System.out.println("octalalLiterals=" +octalalLiterals);
		System.out.println("hexLiterals=" +hexLiterals);
		//print floating point literals
		System.out.println("floatliteral=" +floatliteral);
		//print character literals
		System.out.println("charliteral=" +charliteral);
		// print string literals
		System.out.println("stringliterals=" +stringliterals);
		// print boolean literals
		System.out.println("trueliterals=" +trueliterals);
		System.out.println("falseliterals=" +falseliterals);

	}
}